struct pdb_pointer
{
  uint16_t type;
  union
  {
    struct
    {
      uint32_t ptrtype:5;
      uint32_t ptrmode:3;
      uint32_t isflat32:1;
      uint32_t isvolatile:1;
      uint32_t isconst:1;
      uint32_t isunaligned:1;
      uint32_t isrestrict:1;
      uint32_t size:6;
      uint32_t ismocom:1;
      uint32_t islref:1;
      uint32_t isrref:1;
      uint32_t unused:10;
    } s;
    uint32_t num;
  } attr;
};

